void DWT_Delay_us(uint32_t us) {
    uint32_t startTick = DWT->CYCCNT;
    uint32_t delayTicks = us * (SystemCoreClock / 1000000);
    while (DWT->CYCCNT - startTick < delayTicks);
}

int HCSR04_Read(void) {
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_RESET);
    DWT_Delay_us(2);
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_SET);
    DWT_Delay_us(10);
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_RESET);
    
    uint32_t timeout = DWT->CYCCNT;
    uint32_t timeoutTicks = 20000 * (SystemCoreClock / 1000000);
    
    while (HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_7) == GPIO_PIN_RESET) {
        if (DWT->CYCCNT - timeout > timeoutTicks) return -1;
    }
    
    uint32_t start_time = DWT->CYCCNT;
    while (HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_7) == GPIO_PIN_SET) {
        if (DWT->CYCCNT - start_time > timeoutTicks) return -1;
    }
    
    uint32_t elapsed_us = (DWT->CYCCNT - start_time) / (SystemCoreClock / 1000000);
    return elapsed_us / 58;
}

void SGP30_Init(void) {
    uint8_t cmd[2] = {0x20, 0x03};
    HAL_I2C_Master_Transmit(&hi2c1, 0x58 << 1, cmd, 2, 100);
}

void SGP30_Measure(uint16_t *co2, uint16_t *tvoc) {
    uint8_t cmd[2] = {0x20, 0x08};
    HAL_I2C_Master_Transmit(&hi2c1, 0x58 << 1, cmd, 2, 100);
    HAL_Delay(12);
    uint8_t data[6];
    if (HAL_I2C_Master_Receive(&hi2c1, 0x58 << 1, data, 6, 100) == HAL_OK) {
        *co2 = (data[0] << 8) | data[1];
        *tvoc = (data[3] << 8) | data[4];
    }
}

void set_motors(int speed_left, int speed_right) {
    if (speed_left > 0) {
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13, GPIO_PIN_SET);
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, GPIO_PIN_RESET);
    } else if (speed_left < 0) {
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13, GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, GPIO_PIN_SET);
        speed_left = -speed_left;
    } else {
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13, GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, GPIO_PIN_RESET);
    }
    
    if (speed_right > 0) {
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_SET);
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_1, GPIO_PIN_RESET);
    } else if (speed_right < 0) {
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_1, GPIO_PIN_SET);
        speed_right = -speed_right;
    } else {
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_15, GPIO_PIN_RESET);
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_1, GPIO_PIN_RESET);
    }

    if (speed_left > 999) speed_left = 999;
    if (speed_right > 999) speed_right = 999;
    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, speed_left);
    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, speed_right);
}

void track_face() {
    if (face_detected) {
        int error = target_face_x - 160;
        int base_speed = 300; 
        int turn_speed = error * 3; 
        int left = base_speed + turn_speed;
        int right = base_speed - turn_speed;
        set_motors(left, right);
    } else {
        set_motors(0, 0);
    }
}

