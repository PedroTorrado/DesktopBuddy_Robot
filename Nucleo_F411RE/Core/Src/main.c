void OLED_PrintText(uint8_t page, uint8_t col, char* str) {
    OLED_WriteCommand(0x21); OLED_WriteCommand(col); OLED_WriteCommand(127);
    OLED_WriteCommand(0x22); OLED_WriteCommand(page); OLED_WriteCommand(7);
    static uint8_t tx_buf[6];
    tx_buf[0] = 0x40;
    while(*str) {
        uint8_t idx = 36;
        char c = *str;
        if (c >= '0' && c <= '9') idx = c - '0';
        else if (c >= 'A' && c <= 'Z') idx = (c - 'A') + 10;
        else if (c >= 'a' && c <= 'z') idx = (c - 'a') + 10;
        else if (c == ':') idx = 37;
        for(int i = 0; i < 5; i++) tx_buf[i + 1] = BasicFont[idx][i];
        HAL_I2C_Master_Transmit(&hi2c1, OLED_ADDR, tx_buf, 6, 10);
        str++;
    }
}

